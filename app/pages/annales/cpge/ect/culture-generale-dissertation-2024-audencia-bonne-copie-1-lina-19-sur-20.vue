    <h1>
      Résister à la violence
    </h1>

    <p>
      Dans <span class="underlined">l'étrange histoire du docteur Jekyll</span>, il a été montré que le Dr Jekyll a été incapable de résister à son alter ego,
      présenté sous le nom de Mr Hyde.
      En effet, ce dernier poussait le Dr Jekyll à commettre des actes d'extrême violence,
      lui faisant perdre la raison.
      Ainsi, le destin tragique du Dr Jekyll nous pousse à nous poser la question suivante : est-il possible de résister à la violence ?
      Dire qu'on « résiste à la violence » revient à considérer qu'il existe probablement en nous une volonté d'être violent.
      Or, si « résister » suppose qu'on mettra nos pulsions violentes de côté afin d'éviter le passage à l'acte violent,
      il est donc naturel d'associer la résistance à la violence, à une lutte contre elle.
      Il s'agira alors de la contrôler, en ayant recours à la raison.
      De plus, le sujet tel qu'il a été formulé nous invite à penser la résistance à « la » violence comme celle qui est connue
      et définie par tous.
      Il s'agit donc de l'unique violence destructrice et anéantissante.
      Ainsi, pour traiter ce sujet, nous poserons le problème suivant :
      comment l'Homme peut-il « résister » et lutter contre ses pulsions violentes ?
      Nous verrons dans un premier temps qu'il ne sera pas nécessaire de résister à la violence étant donné qu'elle est inhérente à l'Homme et son évolution.
      Puis, dans un second temps, nous montrerons pourquoi il faut tout de même lutter contre la violence.
      Enfin, nous exposerons les moyens à mobiliser pour résister à la violence.
    </p>

    <p class="text-center mt-4 mb-4">
      * * *
    </p>

    <p>
      La violence a été le moteur principal de l'évolution de l'Homme sur Terre.
      Il paraît alors peu légitime de lutter contre elle car elle lui est inhérente.
    </p>

    <p class="mt-4">
      D'abord, la violence est nécessaire, on n'a nul besoin de lutter contre elle, car c'est elle qui garantit l'évolution de l'Homme.
      Hegel parlera ainsi d'une violence créatrice, génératrice de l'Histoire et de l'évolution humaine.
      C'est elle qui serait à l'origine des évolutions culturelles et sociétales actuelles.
      Elle a en outre permis l'avancement des structures sociétales afin d'aboutir à un monde où la paix règne.
      Ainsi, la violence se résout de manière automatique.
      Norbert Elias dira dans <span class="underlined">processus de civilisation</span>,
      que l'avènement de la violence destructrice aurait conduit aujourd'hui à une baisse considérable de la violence.
    </p>

    <p class="mt-4">
      De plus, la résistance à la violence est superfétatoire car elle annule la volonté des Hommes à faire société.
      En effet, Georg Simmel dira que le conflit est nécessaire car il est structurant.
      Ainsi, la lutte contre la violence n'est autre qu'une négation de la vie en société.
      Il rajoutera que le conflit permet de définir les rapports hiérarchiques et d'établir de manière précise les rapports régissant la société.
      L'exercice de la force reste en cela nécessaire car c'est grâce à elle qu'on pourra déterminer la personne la plus forte,
      capable de dominer.
      La résistance à la violence et la négation du conflit est donc gage de désorganisation de la société.
    </p>

    <p class="mt-4">
      De surcroît, résister à la violence ne serait pas bénéfique
      car la violence est nécessaire dans l'exercice du pouvoir étant donné qu'elle légitime la position du dominant.
      Dans <span class="underlined">Le prince</span>, de Machiavel, on nous montrera que le dirigeant doit être capable de préserver sa position de domination.
      Ainsi, l'exercice de la violence est nécessaire afin d'assurer sa légitimité.
      La nécessité de la violence s'inscrit dans une démarche de garantie de l'ordre et de la paix.
      Seule la soumission à cette violence, exercée par le Prince, permettra l'ordre social au sein des sociétés :
      car si chacun venait à imposer son point de vue, la société disparaîtrait.
      La résistance à la violence sera alors vectrice de désordre.
    </p>

    <p class="text-center mt-4 mb-4">
      * * *
    </p>

    <p>
      Néanmoins, il apparaît tout de même que la résistance à la violence est nécessaire
      afin d'éviter tout abus susceptible de provoquer une dégénérescence de l'acte violent.
    </p>

    <p class="mt-4">
      La résistance à la violence est nécessaire car c'est elle qui permet de faire face à la logique infernale de vengeance.
      En ce sens, Nietzsche théorisera deux formes de vengeance : la vengeance de protection et celle de réparation.
      En effet, la vengeance de protection est nécessaire car elle permet de se protéger contre d'éventuels prochaines attaques.
      Tandis que la vengeance de réparation n'est pas nécessaire et doit faire l'objet de résistance, car elle est destructrice.
      Cette forme de violence a notamment été représentée dans l'œuvre de Shakespeare <span class="underlined">Titus Andronicus</span>.
      En effet, cette œuvre théâtrale montre l'atrocité de la vengeance qui donne suite à des actes d'extrême violence.
      Par exemple, la violence a atteint son apogée lorsque Titus convie Tamora à un festin, où il lui fera manger ses enfants pour le simple désir de vengeance.
      Ici, Titus aurait dû faire preuve de résistance face à l'exercice de la violence afin d'éviter ses abus.
    </p>

    <p class="mt-4">
      De plus, la résistance à la violence est également nécessaire afin d'éviter tout type d'oppression.
      À cet égard, il faut lutter contre la violence pour ne pas laisser place aux régimes totalitaires car :
      « la justice sans la force est impuissante, la force sans la justice est tyrannique ».
      Ainsi, dans son œuvre <span class="underlined">1984</span>,
      George Orwell dépeint l'oppression dans laquelle se trouvait la société londonienne
      notamment à cause du système politique totalitaire du Parti.
      Smith et Julia ont ainsi exercé secrètement un groupe de résistance clandestine.
      Ils ont de ce fait exprimé leur résistance face à la violence, mais en vain
      car ils ont été rattrapés par O'Brien qui les a trahit et remis au Parti pour être soumis à la torture.
    </p>

    <p class="mt-4">
      Enfin, la résistance à la violence est nécessaire étant donné qu'elle permet de préserver la dignité de l'Homme.
      À cet égard, l'Homme devra lutter contre ses pulsions violentes afin de ne pas aboutir à une négation de l'autre.
      Il semblerait, que ce processus se fasse de manière naturelle chez l'Homme grâce à son empathie.
      Frans de Waal a montré que l'empathie est innée chez l'Homme et lui permet de résister à la violence et à son exercice abusif.
      De plus, dans <span class="underlined">Les conditions de l'Homme</span> de Hannah Arendt,
      il a été montré que l'Homme est un être qui cache en lui des pulsions effrayantes,
      exprimant sa volonté de puissance.
      Toutefois, certaines figures de l'œuvre luttent contre ses pulsions et invitent à la préservation de la dignité humaine.
    </p>

    <p class="text-center mt-4 mb-4">
      * * *
    </p>

    <p>
      Enfin, il a été montré que la résistance à la violence est nécessaire.
      Il est de ce fait indispensable de montrer comment il est possible de le faire.
    </p>

    <p class="mt-4">
      D'abord, la lutte contre la violence peut se faire par le dialogue.
      Rawls dira que la violence n'existe que dans un rapport social.
      Il faut alors utiliser le dialogue, selon Eric Weil, afin de mettre fin à la violence.
      Toutefois ce dialogue ne peut aboutir qu'entre individus rationnels et raisonnables qui sont prêts à faire des compromis pour résister à la violence.
      Ce dialogue peut même aboutir à une forme de pardon, permettant de mettre fin à la violence.
      Derrida dira même qu'il faut pardonner l'impardonnable car si on venait à pardonner que ce qui est pardonnable alors,
      le pardon perdra sa valeur.
    </p>

    <p class="mt-4">
      De surcroît, il est possible de résister à la violence à travers sa représentation.
      Ainsi, la catharsis chez Aristote permet de créer une distanciation dans l'exercice de l'acte violent.
      La représentation désinciterait alors à l'exercice de la violence car les individus se mettent à la place de la victime.
      Tel a été le cas dans le film <span class="underlined">Orange mécanique</span> de Kubrick où Alex,
      adolescent violent, a subi une méthode de torture nouvelle pour se libérer de ses pulsions violentes.
      Cette méthode de torture l'obligeait à voir continuellement des scènes d'extrême violence.
      Au final, ce dernier n'était plus violent, la méthode de torture a marché.
      Il n'était même plus capable de se défendre.
      Il paraît donc que la représentation de la violence
      l'a désincité à l'exercer au point qu'il n'était plus capable de voir des scènes violentes.
    </p>

    <p class="mt-4">
      Enfin, la résistance à la violence peut se faire à travers la désobéissance civile.
      En effet, Arendt distinguera entre la torture, la révolution violente et la désobéissance civile.
      Elle dira que le moyen le plus efficace pour que le peuple fasse entendre son mécontentement est la désobéissance civile qui,
      résistant à toute forme de violence, laisse entendre la violence du peuple.
      De plus, la désobéissance civile résiste aux violences exercées par les politiciens qui sont susceptibles de
      recourir abusivement à leur pouvoir en faisant violence aux droits du peuple.
    </p>

    <p class="text-center mt-4 mb-4">
      * * *
    </p>

    <p>
      Pour conclure, il semble que la résistance à la violence est nécessairement permise par la raison.
      De ce fait, l'échec du Dr Jekyll à résister à Mr Hyde témoigne de sa faiblesse.
      De plus, la résistance à la violence est nécessaire afin d'éviter l'oppression,
      la négation de l'autre et le recours abusif à la violence par la vengeance.
      Ainsi, afin de résister à la violence, il est possible de passer par le dialogue, le pardon,
      la catharsis ou encore la désobéissance civile.
      Toutefois, il faudrait préciser que la résistance à la violence n'est pas toujours nécessaire
      car elle est inhérente à l'Homme et à son évolution.
    </p>

