    <h1>
      Sujet n°1 : Est-il naturel d'être violent ?
    </h1>

    <p>
      Dans <span class="underlined">Voyage au bout de la nuit</span>,
      Louis-Ferdinand Céline présente un protagoniste tourmenté par son retour constant à la violence,
      que ce soit celle de la nature avec la maladie et la mort auxquelles il fait face en tant que médecin,
      ou celle des Hommes, avec les guerres, la colonisation et le travail à la chaîne.
      Ce contraste de l'omniprésence et du retour systématique de la violence sape le moral du protagoniste,
      qui finit par conclure qu'elle est une forme de fatalité, que c'est « un tunnel qui finit dans la nuit ».
      On peut alors être amené à se poser la question suivante :
      toute cette violence est-elle naturelle ?
      Cela pourrait en effet expliquer son aspect de fatalité.
    </p>

    <p>
      Au premier abord, on peut penser qu'effectivement la violence est naturelle,
      qu'être violent est un état primaire.
      L'univers lui-même semble être violent : son commencement ayant été marqué selon les modèles théoriques,
      par la gigantesque explosion qu'est le Big Bang.
      À l'échelle de notre planète, la violence s'exprime par des catastrophes naturelles
      ou encore par le principe même de chaîne alimentaire.
      Enfin à une dernière échelle, celle des Hommes, la violence est omniprésente,
      que ce soit dans sa représentation, par les arts, ou dans la réalité avec une histoire parsemée de guerres,
      de génocides et de meurtres.
      Face à tous ces exemples, il semble évident qu'être violent soit naturel.
    </p>

    <p>
      Néanmoins, avant de confirmer une telle affirmation,
      il serait intéressant de comprendre ce qu'être violent signifie.
      Est violent celui qui exerce une force excessive sur quelqu'un ou quelque chose de manière à en abuser.
      Or cette volonté d'abuser suppose une conscience, et le tout être connu doté d'une conscience,
      d'une raison, est l'humain.
      Ainsi, la violence, bien que ce terme soit employé pour désigner du phénomène naturel comme une « tempête violente »,
      ne serait en réalité que propre à l'être humain.
      Et, étant doté d'un libre arbitre, la violence apparaît comme un choix délibéré de sa part,
      un acquis et non un inné.
      On peut donc remettre en question la dimension naturelle de la violence.
    </p>

    <p>
      Dans une première partie, nous étudierons en quoi la violence est inscrite dans la nature même de l'Homme,
      puis nous nous interrogerons sur lien entre déterminisme et violence,
      avant de voir, dans une dernière partie, la raison pour laquelle être violent est un acquis,
      et non quelque chose de naturel.
    </p>

    <p class="center">
      * * *
    </p>

    <p>
      Dans la plupart des cultures, la violence intervient dès les prémices de l'humanité.
      Dans la Bible par exemple, le premier meurtre, celui d'Abel par Caïn,
      est réalisé dès la première génération d'humains engendrés par des humains,
      et cette violence se perpétue ensuite tout au long de l'histoire de l'humanité.
      Cette apparition précoce de la violence a une cause : selon la vision chrétienne,
      la nature de l'homme a été entachée par le péché originel.
    </p>

    <p>
      Cette idée de violence inscrite dans la nature humaine se retrouve dans
      <span class="underlined">Le citoyen</span> de Hobbes et dans <span class="underlined">La route</span>
      de Cormac Mc Carthy.
      Tous deux réalisent un exercice de pensée en essayant d'imaginer ce que seraient les sociétés humaines
      si elles n'étaient pas encadrées et organisées autour d'un État,
      l'un au travers d'une réflexion philosophique,
      l'autre au travers d'un roman.
      Hobbes estime qu'en l'absence d'État, les individus reviendraient à une forme d' « état de nature »,
      c'est-à-dire un retour au primitif dans lequel la nature profonde de l'homme peut s'exprimer.
      Il en résulterait alors une « guerre de chacun contre chacun »,
      la violence ne serait plus contenue par aucune forme d'autorité.
      Hobbes reprend cette locution latine « Homo homini lupus » : « l'homme est un loup pour l'homme ».
      La violence apparaît donc comme naturelle chez l'être humain.
      Cormac Mc Carthy, quant à lui, décrit un monde après l'effondrement et le démantèlement des institutions étatiques.
      La société parvient vite à une forme de chaos duquel un père et son fils tentent de s'extraire,
      devant échapper au cannibalisme et au viol.
    </p>

    <p>
      Chez Freud, la nature humaine est soumise à des pulsions,
      les unes constructrices, comme les pulsions de vie,
      les autres au contraire destructrices, comme les pulsions de mort.
      C'est par ces dernières que s'exprime la violence, qui tend à l'annihilation.
    </p>

    <p>
      Fort de ces exemples, on peut être amené à envisager que la violence soit inhérente à la nature même de l'Homme,
      et ce dès ses origines.
      Être violent pourrait alors être considéré comme un état naturel.
      On peut toutefois se demander si la violence ne s'est pas acquise à un moment donné,
      puis transmise aux générations suivantes.
    </p>

    <p class="center">
      * * *
    </p>

    <p>
      Dans la bibliographie d'Émile Zola, on retrouve un ensemble d'œuvres qui constituent le cycle des Rougon-Macquart,
      où chaque livre présente des membres de cette famille.
      Chaque protagoniste est condamné à subir une fatalité héréditaire selon le principe du déterminisme :
      chaque individu est déterminé par son hérédité.
      Ainsi, pour donner un exemple, un fils d'alcoolique sera alcoolique.
      Dans <span class="underlined">La bête humaine</span>,
      le protagoniste a hérité d'une nature violente par laquelle
      il est rongé et contre laquelle il ne parvient pas à lutter,
      ce qui le mène dans une folie meurtrière qui le conduit à tuer sa femme.
      Il y aurait donc une hérédité de la violence, intégrant celle dernière à la nature de certains Hommes.
    </p>

    <p>
      Une forme de déterminisme contextuel existe aussi.
      C'est ce que l'on peut par exemple constater dans
      <span class="underlined">Tropiques de la violence</span> de Natacha Appanah.
      Elle y raconte l'histoire de Moïse, un jeune orphelin recueilli par une impératrice à Mayotte.
      Cette dernière meurt prématurément, le laissant livré à lui-même alors qu'il n'est encore qu'un adolescent.
      Très vite il tombe dans la délinquance, entouré de jeunes habitant dans un bidonville.
      Malgré une opportunité pour s'extirper de sa condition,
      celle-ci semble inévitablement le rappeler à elle :
      le contexte dans lequel il vit a fini par le déterminer.
    </p>

    <p>
      Au-delà du déterminisme, d'autres éléments peuvent modifier la nature d'un Homme.
      Dans ses <span class="underlined">Confessions</span>,
      Saint Augustin partage l'histoire de son ami Alypius.
      Ce dernier était réticent à l'idée d'assister aux combats de gladiateurs,
      qu'il jugeait trop violents,
      et refusait catégoriquement toute invitation.
      Cependant un jour, sous la pression de son entourage,
      il cèda et, face à ce spectacle, il ressentit non pas du dégoût mais plutôt une forme de fascination.
      Il avait pris goût à la violence.
      Pour donner un autre exemple, on peut évoquer <span class="underlined">Orange mécanique</span> de Kubrick.
      Après la sortie de ce film, qui a marqué les esprits du fait de l'ultraviolence qu'il représente,
      de nombreuses personnes s'en sont servies pour justifier leurs actes violents,
      comme s'ils avaient agi par mimétisme.
      Ainsi on peut dire qu'on peut, d'une certaine manière, s'éduquer à la violence.
      Notre nature pourrait alors évoluer et intégrer des caractères violents, comme acquis.
    </p>

    <p>
      Toutefois, bien que cette violence semble naturelle, l'est-elle réellement ?
    </p>

    <p class="center">
      * * *
    </p>

    <p>
      La violence est propre à l'être humain, du fait de la nécessité de la présence d'une conscience pour la motiver.
      Or ce même être humain est le seul animal doté de raison, et par conséquent de libre arbitre,
      ce qui lui permet de réaliser du choix délibérés.
      En partant de ce postulat, il est alors tout à fait possible de faire des choix
      en désaccord avec notre nature profonde.
    </p>

    <p>
      Comme l'affirme Sartre dans <span class="underlined">ses Cahiers pour une morale</span>,
      la violence est quelque chose d'anormal, en dehors de ce qu'il qualifie de « legalité ».
      Il illustre cela par l'exemple de la bouteille : retirer le bouchon est dans l'ordre du choses
      tandis que briser le goulot sort de la norme, de la legalité.
    </p>

    <p>
      Un tel comportement est nihiliste, il tend à la destruction.
      Or le vivant est censé être animé par un esprit de conservation, qui semble naturel.
      La violence semble donc ne pas être naturelle.
    </p>

    <p>
      Dans <span class="underlined">Les caves du Vatican</span>, André Gide met en évidence la notion d'acte gratuit.
      Le protagoniste, Lafcadio, n'est pas un homme de nature violente.
      Néanmoins, afin de mettre à l'épreuve son libre arbitre, il choisit de manière parfaitement délibérée,
      après un long raisonnement, de tuer un inconnu, sans aucune raison, en le poussant d'un train.
      Un tel acte n'était pas du tout déterminé par sa nature profonde : c'est une décision personnelle, artificielle.
    </p>

    <p>
      D'autres éléments peuvent contribuer à rendre violent.
      Par exemple, là où Hobbes considère un homme naturellement violent réfréné par l'État,
      Rousseau au contraire, dans son <span class="underlined">Discours sur l'origine et les fondements de l'inegalité parmi les hommes</span>,
      pense que l'Homme naît bon et que c'est la société qui corrompt son cœur, qui le dénature.
      La notion de propriété est à ses yeux le principal moteur de la violence en ce sens où elle engendre une disproportion de l'ego,
      appelant des réponses démesurées lorsque cet ego est offensé.
    </p>

    <p>
      Ainsi, ce seraient l'exercice du libre arbitre et la vie en société qui permettraient la violence.
    </p>

    <p class="center">
      * * *
    </p>

    <p>
      Initialement, nous cherchions à savoir s'il était naturel d'être violent.
      Après réflexion, même si l'homme semble avoir naturellement des penchants pour la violence,
      et que cette dernière peut être motivée par des facteurs comme l'hérédité ou le contexte dans lequel on vit,
      il semble tout de même que la violence résulte d'un choix opéré par le biais du libre arbitre,
      sans pour autant que la nature profonde de l'être violent soit elle-même violente.
      Ainsi on ne serait pas violent mais on pourrait plutôt le devenir.
    </p>

